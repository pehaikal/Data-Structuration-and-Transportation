CREATE TABLE contacts
(
    contact_email      varchar(50) NOT NULL,
    contact_first_name varchar(20) NOT NULL,
    contact_last_name  varchar(20) NOT NULL,
    contact_address    varchar(50) NOT NULL,
    contact_city       varchar(25) NOT NULL,
    contact_country    varchar(5)  NOT NULL,
    contact_birthdate  varchar(15) NOT NULL
)